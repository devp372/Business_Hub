public class Security {
    String q1;
    String q2;
    String ans1;
    String ans2;

    public Security(String q1, String ans1, String q2, String ans2) {
        this.q1 = q1;
        this.q2 = q2;
        this.ans1 = ans1;
        this.ans2 = ans2;
    }

    public String getAns1() {
        return ans1;
    }

    public String getAns2() {
        return ans2;
    }

    public String getQ1() {
        return q1;
    }

    public String getQ2() {
        return q2;
    }

}
